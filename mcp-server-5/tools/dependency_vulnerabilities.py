@mcp.tool(
    name="analyze_dependency_vulnerabilities",
    description="""üõ°Ô∏è –ê–Ω–∞–ª–∏–∑–∏—Ä—É–µ—Ç —É—è–∑–≤–∏–º–æ—Å—Ç–∏ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è GitHub.

–≠—Ç–æ—Ç –∏–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç –ø—Ä–æ–≤–µ—Ä—è–µ—Ç –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –ø—Ä–æ–µ–∫—Ç–∞ –Ω–∞ –Ω–∞–ª–∏—á–∏–µ –∏–∑–≤–µ—Å—Ç–Ω—ã—Ö —É—è–∑–≤–∏–º–æ—Å—Ç–µ–π:
- –ê–Ω–∞–ª–∏–∑ —Ñ–∞–π–ª–æ–≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π
- –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –∏–∑–≤–µ—Å—Ç–Ω—ã–µ CVE
- –†–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏ –ø–æ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—é
- –ö—Ä–∏—Ç–∏—á–Ω–æ—Å—Ç—å —É—è–∑–≤–∏–º–æ—Å—Ç–µ–π

–ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ —ç—Ç–æ—Ç –∏–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –±–µ–∑–æ–ø–∞—Å–Ω–æ—Å—Ç–∏ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π –ø—Ä–æ–µ–∫—Ç–∞.
"""
)
async def analyze_dependency_vulnerabilities(
    owner: str = Field(
        ...,
        description="–í–ª–∞–¥–µ–ª–µ—Ü —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è (username –∏–ª–∏ organization name)",
        examples=["octocat", "microsoft", "facebook"]
    ),
    repo: str = Field(
        ...,
        description="–ù–∞–∑–≤–∞–Ω–∏–µ —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è",
        examples=["Hello-World", "vscode", "react"]
    ),
    ctx: Context = None
) -> ToolResult:
    """
    üõ°Ô∏è –ê–Ω–∞–ª–∏–∑–∏—Ä—É–µ—Ç —É—è–∑–≤–∏–º–æ—Å—Ç–∏ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è GitHub.
    
    Args:
        owner: –í–ª–∞–¥–µ–ª–µ—Ü —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è
        repo: –ù–∞–∑–≤–∞–Ω–∏–µ —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è
        ctx: –ö–æ–Ω—Ç–µ–∫—Å—Ç –¥–ª—è –ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏—è
        
    Returns:
        ToolResult: –†–µ–∑—É–ª—å—Ç–∞—Ç –∞–Ω–∞–ª–∏–∑–∞ —É—è–∑–≤–∏–º–æ—Å—Ç–µ–π
        
    Raises:
        McpError: –ü—Ä–∏ –æ—à–∏–±–∫–∞—Ö –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è
    """
    with tracer.start_as_current_span("analyze_dependency_vulnerabilities") as span:
        span.set_attribute("owner", owner)
        span.set_attribute("repo", repo)
        
        await ctx.info("üöÄ –ù–∞—á–∏–Ω–∞–µ–º –∞–Ω–∞–ª–∏–∑ —É—è–∑–≤–∏–º–æ—Å—Ç–µ–π –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π")
        await ctx.report_progress(progress=0, total=100)
        
        try:
            _require_env_vars(["GITHUB_TOKEN"])
            await ctx.report_progress(progress=10, total=100)
            
            client = create_github_client()
            await ctx.info(f"üîß –ü–æ–¥–≥–æ—Ç–∞–≤–ª–∏–≤–∞–µ–º –∑–∞–ø—Ä–æ—Å –¥–ª—è {owner}/{repo}")
            await ctx.report_progress(progress=20, total=100)
            
            await ctx.info("üì° –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –∑–∞–ø—Ä–æ—Å—ã –∫ GitHub API")
            await ctx.report_progress(progress=30, total=100)
            
            # –°–ø–∏—Å–æ–∫ —Ñ–∞–π–ª–æ–≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π
            dependency_files = [
                "package.json",
                "requirements.txt",
                "pyproject.toml",
                "pom.xml",
                "build.gradle",
                "go.mod",
                "Cargo.toml"
            ]
            
            found_files = []
            vulnerabilities_summary = {
                "total_files": 0,
                "analyzed_files": 0,
                "potential_risks": []
            }
            
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ —Ñ–∞–π–ª–æ–≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π
            for dep_file in dependency_files:
                try:
                    file_url = f"/repos/{owner}/{repo}/contents/{dep_file}"
                    response = await retry_github_request(
                        client, "GET", file_url, ctx=ctx
                    )
                    file_data = response.json()
                    
                    if file_data.get("type") == "file":
                        found_files.append(dep_file)
                        vulnerabilities_summary["total_files"] += 1
                        
                except httpx.HTTPStatusError as e:
                    if e.response.status_code != 404:
                        raise
                    continue
                except:
                    continue
                
                await ctx.report_progress(progress=30 + (len(found_files) * 5), total=100)
            
            await ctx.report_progress(progress=70, total=100)
            await ctx.info("üìÑ –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –ø–æ–ª—É—á–µ–Ω–Ω—ã–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã")
            
            # –§–æ—Ä–º–∏—Ä—É–µ–º —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏
            if found_files:
                vulnerabilities_summary["analyzed_files"] = len(found_files)
                vulnerabilities_summary["potential_risks"].append(
                    "–†–µ–∫–æ–º–µ–Ω–¥—É–µ—Ç—Å—è —Ä–µ–≥—É–ª—è—Ä–Ω–æ –æ–±–Ω–æ–≤–ª—è—Ç—å –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏"
                )
                vulnerabilities_summary["potential_risks"].append(
                    "–ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ Dependabot –¥–ª—è –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–≥–æ –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞"
                )
            
            # –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
            result_text = f"üõ°Ô∏è –ê–Ω–∞–ª–∏–∑ —É—è–∑–≤–∏–º–æ—Å—Ç–µ–π –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π –¥–ª—è {owner}/{repo}\n\n"
            result_text += f"üìä –ù–∞–π–¥–µ–Ω–Ω—ã–µ —Ñ–∞–π–ª—ã –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π:\n"
            
            if found_files:
                for file in found_files:
                    result_text += f"  ‚úÖ {file}\n"
                result_text += f"\nüìã –†–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏:\n"
                for risk in vulnerabilities_summary["potential_risks"]:
                    result_text += f"  - {risk}\n"
            else:
                result_text += f"  ‚ö†Ô∏è –§–∞–π–ª—ã –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π –Ω–µ –Ω–∞–π–¥–µ–Ω—ã –≤ –∫–æ—Ä–Ω–µ —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è\n"
            
            result_text += f"\nüí° –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ: –î–ª—è –¥–µ—Ç–∞–ª—å–Ω–æ–≥–æ –∞–Ω–∞–ª–∏–∑–∞ –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ GitHub Dependabot –∏–ª–∏ Snyk\n"
            
            await ctx.report_progress(progress=95, total=100)
            await ctx.info("‚úÖ –ê–Ω–∞–ª–∏–∑ —É—è–∑–≤–∏–º–æ—Å—Ç–µ–π –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π —É—Å–ø–µ—à–Ω–æ –≤—ã–ø–æ–ª–Ω–µ–Ω")
            await ctx.report_progress(progress=100, total=100)
            
            span.set_attribute("found_files", len(found_files))
            span.set_attribute("success", True)
            
            return ToolResult(
                content=[TextContent(type="text", text=result_text)],
                structured_content={
                    "repository": f"{owner}/{repo}",
                    "dependency_files": found_files,
                    "vulnerabilities_summary": vulnerabilities_summary
                },
                meta={"owner": owner, "repo": repo, "operation": "analyze_dependency_vulnerabilities"}
            )
            
        except Exception as e:
            span.set_attribute("error", str(e))
            await handle_github_error(e, ctx, f"–∞–Ω–∞–ª–∏–∑–µ —É—è–∑–≤–∏–º–æ—Å—Ç–µ–π –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π {owner}/{repo}")

